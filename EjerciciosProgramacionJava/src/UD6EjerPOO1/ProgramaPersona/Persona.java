package UD6EjerPOO1.ProgramaPersona;

public class Persona {

    public String dni;
    public String nombre;
    public String apellidos;
    public int edad;
    
    public Persona(String d, String n, String a, int e) {
        nombre = n;
        apellidos = a;
        dni = d;
        edad = e;
    }
    
}
